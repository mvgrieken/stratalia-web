export function getTranslationMap(): Record<string, string> {
  return STRAATTAAL_TO_NL;
}

export function getReverseTranslationMap(): Record<string, string> {
  return NL_TO_STRAATTAAL;
}

export function findTranslation(text: string, direction: 'to-nl' | 'to-straattaal' = 'to-nl'): string | null {
  const normalizedText = text.toLowerCase().trim();
  const map = direction === 'to-nl' ? STRAATTAAL_TO_NL : NL_TO_STRAATTAAL;
  
  // Direct match
  if (map[normalizedText]) {
    return map[normalizedText];
  }
  
  // Partial match
  for (const [key, value] of Object.entries(map)) {
    if (key.includes(normalizedText) || normalizedText.includes(key)) {
      return value;
    }
  }
  
  return null;
}
